#include "libft.h"

void	ft_putendl_fd(char *s, int fd) // prida text do filu nasledovany nulou
{
	size_t	i;

	i = 0;
	if (s == NULL)
		return ;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
